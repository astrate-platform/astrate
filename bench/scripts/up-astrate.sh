#!/usr/bin/env bash
# Brings up the Astrate benchmark stack (bench/astrate/compose.yml):
# generates the housekeeping JWT keypair and a self-signed broker server
# certificate, writes the mounted config, builds the image, and waits for
# readiness. Idempotent — existing keys are kept so state files stay valid.
set -euo pipefail

cd "$(dirname "$0")/.."
mkdir -p keys astrate/conf

# Housekeeping API keypair: the private half is what `bench provision
# -housekeeping-key keys/housekeeping.pem` signs with.
if [[ ! -f keys/housekeeping.pem ]]; then
    openssl genrsa -out keys/housekeeping.pem 2048 2>/dev/null
    openssl rsa -in keys/housekeeping.pem -pubout -out astrate/conf/housekeeping.pub 2>/dev/null
    echo "generated keys/housekeeping.pem"
fi

# Self-signed broker server certificate (devices skip verification, exactly
# like against Astarte's compose snakeoil certs).
if [[ ! -f astrate/conf/broker.crt ]]; then
    openssl req -x509 -newkey ec -pkeyopt ec_paramgen_curve:P-256 -nodes \
        -keyout astrate/conf/broker.key -out astrate/conf/broker.crt \
        -days 365 -subj "/CN=astrate-bench-broker" \
        -addext "subjectAltName=IP:127.0.0.1,DNS:localhost" 2>/dev/null
    echo "generated astrate/conf/broker.{crt,key}"
fi

cat > astrate/conf/bench.toml <<'EOF'
# Generated by scripts/up-astrate.sh — mounted at /etc/astrate/bench.toml.
[mqtt]
tls_cert_file = "/etc/astrate/broker.crt"
tls_key_file  = "/etc/astrate/broker.key"

[housekeeping]
jwt_public_key_files = ["/etc/astrate/housekeeping.pub"]
EOF

docker compose -f astrate/compose.yml up -d --build

echo -n "waiting for readiness"
for _ in $(seq 1 60); do
    if curl -fsS http://127.0.0.1:8080/astrate/v1/readiness >/dev/null 2>&1; then
        echo " — up"
        echo "API:    http://127.0.0.1:8080"
        echo "Broker: mqtts://127.0.0.1:8883"
        echo "Next:   go run . provision -base-url http://127.0.0.1:8080 -housekeeping-key keys/housekeeping.pem"
        exit 0
    fi
    echo -n .
    sleep 2
done
echo " — FAILED (docker compose -f astrate/compose.yml logs astrate)"
exit 1
